# Es el famoso primer paso que todo programador hace en un nuevo lenguaje
# Consiste en imprimir en pantalla la frase: ¡Hola, mundo!

print("¡Hola, mundo!") # salida: ¡Hola, mundo!
print("Hello, World!") # salida: Hello, World!

# La función 'print()' en inglés significa 'imprimir', pero en programación no se refiere
# a imprimir en papel, sino a mostrar información en la pantalla o consola.
# Antiguamente, cuando las máquinas no tenían pantallas, la información se "imprimía"
# en tarjetas perforadas, de ahí que se mantenga el término 'print' hasta el día de hoy

