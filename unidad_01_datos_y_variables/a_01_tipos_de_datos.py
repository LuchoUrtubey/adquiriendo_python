# En Python los tipos de datos se pueden dividir en dos grandes grupos
# -Primitivos
# -Compuestos

# Por ahora nos vamos a concentrar en los primitivos que son:

# --------------------------------------------------------------------------------------------------
# STRINGS (STR)
# --------------------------------------------------------------------------------------------------

# Son cadenas de texto, y se escriben entre comillas dobles o simples
# Es el que probamos anteriormente imprimiendo ¡Hola mundo!
"¡Hola, mundo!"
'¡Hola, mundo!'

# --------------------------------------------------------------------------------------------------
# INTEGER (INT)
# --------------------------------------------------------------------------------------------------

# Son números enteros, tanto positivos como negativos, y se escriben sin comillas
43
-3

# --------------------------------------------------------------------------------------------------
# FLOAT
# --------------------------------------------------------------------------------------------------

# Son números decimales, tanto negativos como positivos, también conocidos como números flotantes
23.5
11.0

# --------------------------------------------------------------------------------------------------
# COMPLEJOS (COMPLEX)
# --------------------------------------------------------------------------------------------------

# Son los números complejos que tiene la parte real y la parte imaginaria
# Por convención la parte imaginaria se representa con una j

3 + 6j

# --------------------------------------------------------------------------------------------------
# BOOLEANS (BOOL)
# --------------------------------------------------------------------------------------------------

# Los valores Booleanos son dos, True o False
# En Python esos valores comienzan con mayúsculas y sin comillas
True
False

# --------------------------------------------------------------------------------------------------
# NONE (NoneType)
# --------------------------------------------------------------------------------------------------

# Se usa cuando no queremos asignarle un valor a la variable
# Es el equivalente a NULL de otros lenguajes
# Comienza con mayúscula y sin comillas
None
